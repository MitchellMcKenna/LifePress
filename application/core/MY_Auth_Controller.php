class MY_Auth_Controller extends MY_Controller {
    function __construct() {
        parent::__construct();

        if ($this->data->user === FALSE) {
            header('Location: '.$this->config->item('base_url').'admin/login');
            exit();
        } else {
            return;
        }
    }
}

